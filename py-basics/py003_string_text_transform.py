# 字符串 大小写转换
name = "ada lovelace"
# titile() 首字母大写
print(name.title())  # Ada Lovelace

# upper()   全部大写
print(name.upper());  # ADA LOVELACE

# lower()   全部小写
print(name.lower());  # ada lovelace

# 合并字符串
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name
print(full_name)  # ada lovelace
